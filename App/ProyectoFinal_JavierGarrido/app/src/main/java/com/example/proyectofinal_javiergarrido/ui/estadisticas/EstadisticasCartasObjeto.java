package com.example.proyectofinal_javiergarrido.ui.estadisticas;

public class EstadisticasCartasObjeto {
    private int victorias;
    private int derrotas;
    private int total_intentos;
    private String nivel;

    public int getVictorias() {
        return victorias;
    }

    public void setVictorias(int victorias) {
        this.victorias = victorias;
    }

    public int getDerrotas() {
        return derrotas;
    }

    public void setDerrotas(int derrotas) {
        this.derrotas = derrotas;
    }

    public int getTotalIntentos() {
        return total_intentos;
    }

    public void setTotalIntentos(int totalIntentos) {
        this.total_intentos = totalIntentos;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }
}
